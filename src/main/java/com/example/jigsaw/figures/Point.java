package com.example.jigsaw.figures;

/**
 * The type Point.
 */
public class Point {
    /**
     * The X.
     */
    int x;
    /**
     * The Y.
     */
    int y;

    /**
     * Instantiates a new Point.
     *
     * @param x the x
     * @param y the y
     */
    Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    /**
     * Gets x.
     *
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * Gets y.
     *
     * @return the y
     */
    public int getY() {
        return y;
    }
}
